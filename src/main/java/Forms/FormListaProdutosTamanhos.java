    public List<produtoTamanhos> getAllTmanhos(String nome) {
        EntityManager em = JPAUtil.conectar();
    try {
        Query query = em.createQuery(
            "SELECT pt FROM ProdutoTamanhos pt JOIN Produto p"
                    + " ON pt.idProduto = p.id WHERE p.nome = :nome");
        query.setParameter("nome", nome);
        return query.getResultList();
    } finally {
        if (em != null) {
            em.close();
        }
    }
}
    
    
