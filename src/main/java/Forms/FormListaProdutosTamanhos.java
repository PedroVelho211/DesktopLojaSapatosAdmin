    public void displayTable(List<produtoTamanhos> usuarios, JTable jTable) {
    // Create a DefaultTableModel with column names
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    model.addColumn("Tamanho");
    model.addColumn("Estoque");

    // Populate the table model with data from the Usuario list
    for (produtoTamanhos pT : usuarios) {
        model.addRow(new Object[]{
            
            pT.getTamanho(),
            pT.getQuantidade()
            
            
        });
    }

    // Assign the model to the JTable
    jTable.setModel(model);
}
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
